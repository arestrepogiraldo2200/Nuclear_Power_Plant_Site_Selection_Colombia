// Takes the data above and draws the exclusion circle zones and aerial routes
function draw_exclusion_flights(colors1, colors2){

      for (let i = 0; i < flights_coords.length; i++){
            addPolylineToMapFlights(map, flights_coords[i].lati, flights_coords[i].lngi, flights_coords[i].latf, flights_coords[i].lngf, colors1);
      }


      for (let i = 0; i < airports_coords.length; i++){

            let radius = 0;

            if (airports_coords[i].type == 'small_airport'){
                  radius = 8000;    // change to 8000 in case of further consideration
            } else {
                  radius = 16000;   
            }

            let circle = new H.map.Circle({ lat: airports_coords[i].lat, lng: airports_coords[i].lng}, radius, {
                  style: {
                        fillColor: colors2,
                        strokeColor: colors2,
                        lineWidth: 1,
                        opacity: 0.1
                  }
      });

      map.addObject(circle);

      }
}

// Function to draw routes
function addPolylineToMapFlights(map, lati, lngi, latf, lngf, colors1) {
      
      var lineString = new H.geo.LineString();
      
      lineString.pushPoint({lat:lati, lng:lngi});
      lineString.pushPoint({lat:latf, lng:lngf});
      
      map.addObject(new H.map.Polyline(
            lineString, { style: { lineWidth: 1, strokeColor: colors1}}
      ));
}
